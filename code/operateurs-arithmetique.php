<?php
echo 1 + 1;
echo PHP_EOL;

echo 2 - 2;
echo PHP_EOL;

echo 3 * 3;
echo PHP_EOL;

echo 4 / 4;
echo PHP_EOL;

// l'opérateur modulo
// cet opérateur calcul le reste de la division
echo 5 % 3;
echo PHP_EOL;

// opérateur `**` (puissance) disponible depuis php >= 5.6.0
// pour les versions de php < 5.6.0, utiliser la fonction `pow()`
echo 2 ** 2;
echo PHP_EOL;

